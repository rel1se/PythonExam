import math


def f(x, y, z):
    sum = 0
    n = len(x) - 1
    for i in range(1, n + 2):
        sum += 41 * (80 * z[math.ceil(i / 2 - 1)] - y[n + 1 - i] ** 3 - 5 * x[i - 1] ** 2) ** 6
    return sum


print(f([0.59, 0.31, -0.45, -0.62, -0.78, -0.73, 0.02], [-0.24, -0.62, -0.88, -0.03, 0.73, -0.54, 0.06],
        [0.53, 0.62, -0.37, 0.79, 0.34, -0.94, -0.91]))
