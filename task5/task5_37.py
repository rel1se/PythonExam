import math


def f(y):
    n = len(y) - 1
    sum = 0
    for i in range(1, n + 2):
        sum += math.tan(y[n + 1 - math.ceil(i / 3)] + 1) ** 7
    return sum


print(f([-0.77, 0.44, 0.01, 0.25, -0.38, 0.12]))
print(f([-0.98, 0.52, -0.01, -0.81, 0.75, -0.49]))
print(f([0.04, 0.23, -0.07, 0.39, 0.95, -0.23]))
print(f([0.01, -0.71, 0.38, 0.72, 0.29, 0.21]))
print(f([-0.52, -0.98, -0.27, -0.44, 0.1, 0.62]))
