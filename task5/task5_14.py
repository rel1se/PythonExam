import math


def f(y, x, z):
    sum = 0
    n = len(x) - 1
    for i in range(1, n + 2):
        sum += 49 * pow(math.tan(29 * y[i - 1] ** 2 - 26 * pow(z[n + 1 - math.ceil(i / 2)], 3) - x[i - 1]), 5)
    return sum


